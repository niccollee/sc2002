/**
 * Attributes of a Student used as keys for filtering and sorting.
 *
 * Each constant represents a field or derived value that can be supplied
 * to StudentFilter and StudentSorter to select or order students.
 */
public enum StudentAttributes {
    ID,
    NAME,
    YEAROFSTUDY,
    MAJOR,
    APPLIEDINTERNSHIP,
    ACCEPTEDINTERNSHIP
}
